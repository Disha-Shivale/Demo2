#Print all list members
a = ['Item 1', 'Item 2', 'Item 3', 'Item 4', 'Item 5', 'Item 6']
print(a)
print()

#Access List Members
print(a[3])
print()

#Access Negative Value
print(a[-1])
print()

#Range
print(a[0:3])
print()

#Not specifying start value
print(a[:4])
print()

#Not specifying end value
print(a[1:])
print()

#Change Item value
a[1] = 'Felix ITs'
print(a)
print()





